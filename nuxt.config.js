import axios from 'axios'

const dynamicRoutes = async () => {
  const users = await axios.get('http://localhost:8888/api/users')
  const usersPage = users.data.map(user => {
    return {
      route: `/user2/${user.uid}`
      // payload: user
    }
  })

  return [
    ...usersPage
  ]
}

